package by.jonline.task1.main;

public class Number01 {

	public static void main(String[] args) {
		
		double x;
		double y;
		
		x = 17;
		y = -81;
		
		double sum;
		sum = x + y;
		
		double razn;
		razn = x - y;
		
		double proizv;
		proizv = x * y;
		
		double chastn;
		chastn = x / y;
		
		System.out.println("x = " + x + ", y = " + y);
		System.out.println("x + y = " + sum);
		System.out.println("x - y = " + razn);
		System.out.println("x * y = " + proizv);
		System.out.println("x / y = " + chastn);
				

	}

}
